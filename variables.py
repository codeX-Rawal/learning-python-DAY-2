# Day 2: Variables in Python

# Integer variable
age = 18
print("Age:", age)

# Float variable
height = 5.8
print("Height:", height)

# String variable
name = "Rawal"
print("Name:", name)

# Boolean variable
is_student = True
print("Is Student:", is_student)

# Taking input from user
city = input("Enter your city: ")
print("City:", city)

# Updating variable
age = age + 1
print("Age after one year:", age)
